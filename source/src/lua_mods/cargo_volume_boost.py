"""Cryovac Cargo Volume Boost — scales DumpVolume on every live
UMTVehicleCargoSpaceComponent whose CargoSpaceType matches one of the
targeted families (Tanker, Dump, DryBulk, Garbage). Polls every
5 seconds so freshly-spawned vehicles get the boost too.

This is the runtime Lua equivalent of the old "More Cargo" pak mod
(which was a static asset override of specific vehicle Blueprints).
Using Lua instead means: one mod covers every vehicle of each type,
not just the single Blueprint the pak replaced, and you can retune
the multiplier without recompiling assets.
"""
