        "  1. ProductionConfigs[].ProductionSpeedMultiplier — multiplied\n"
        "     by the Production Speed slider value.\n\n"
        "  2. StorageConfigs[].MaxStorage AND the delivery point's\n"
        "     top-level MaxStorage — multiplied by the Warehouse\n"
        "     Storage slider value (rounded to the nearest integer).\n\n"
        "Baselines per (delivery_point, array_index) are captured on\n"
        "first sighting so repeated polls don't compound the effect\n"
        "on the same instance. Newly-loaded delivery points are\n"
        "baselined + scaled on the next poll.\n\n"
        "MULTIPLAYER\n"
        "-----------\n"
        "This mod runs server-side. The fields it modifies are not\n"
