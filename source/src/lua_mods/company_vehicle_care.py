"""Cryovac Company Vehicle Care — reshapes CompanyAI running-cost and
condition-usage multipliers on every FVehicleRow across all Vehicles*
DataTables. Three independent mode factors so the player can tune
running cost, on-road wear, and offroad wear separately."""
