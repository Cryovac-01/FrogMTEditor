_MAIN_LUA_TEMPLATE = '''--[[
    Cryovac: Company Profit Boost
    =============================
    Boosts the owner-profit-share payout ONLY on vehicles your
    company owns. Non-company vehicles stay vanilla.

    Mechanism: pre-hook on
        MotorTownPlayerController:ServerGiveOwnerProfitShare(
            Vehicle, Money, TxType)
    When Vehicle.Net_OwnerCompanyGuid matches a company the local
    player owns, we rewrite the Money parameter IN PLACE to
    floor(Money * Multiplier) before vanilla executes. Vanilla then
    pays out the boosted amount as a single transaction, so both the
    on-screen popup and the company ledger reflect the real total.

    Generated by Frog Mod Editor. Multiplier is hardcoded below;
    regenerate from Frog to change it.

    Runs on SERVER (host / dedicated). Requires UE4SS.
]]--

local CONFIG = {{
    Multiplier = {multiplier},

    -- How frequently we rediscover the player's owned-company list
    -- (handles newly-founded or left companies mid-session).
    OwnerRescanEverySec = 30,
}}

local function Log(msg) print("[CryovacCompanyProfitBoost] " .. msg) end

local function SafeGet(obj, prop)
    local ok, val = pcall(function() return obj[prop] end)
    if ok then return val end
    return nil
end

local function Unwrap(obj)
    if obj == nil then return nil end
    -- UE4SS wraps primitives and structs as accessors. Try :get() first,
    -- fall back to the raw value if it's already unwrapped.
    local ok, val = pcall(function() return obj:get() end)
    if ok and val ~= nil then return val end
    return obj
end

local function GuidsEqual(g1, g2)
    if not g1 or not g2 then return false end
    return (SafeGet(g1, "A") == SafeGet(g2, "A"))
       and (SafeGet(g1, "B") == SafeGet(g2, "B"))
       and (SafeGet(g1, "C") == SafeGet(g2, "C"))
       and (SafeGet(g1, "D") == SafeGet(g2, "D"))
end

local function GuidAsString(g)
    if not g then return "<nil>" end
    return string.format("%08x-%08x-%08x-%08x",
        SafeGet(g, "A") or 0, SafeGet(g, "B") or 0,
        SafeGet(g, "C") or 0, SafeGet(g, "D") or 0)
end

-- Cached list of (MTCompanySystem, CompanyGuid) for the local player's
-- owned companies. Refreshed on a slow cadence so newly-founded
-- companies start getting the boost without requiring a relaunch.
local ownedCompanies = {{}}

-- Field-name cache for MTCompany's owner-character GUID. Motor Town's
-- MTCompany struct exposes the owner under a name that varies across
-- builds (and at least one variant -- "Net_OwnerCharacterGuid" -- is
-- registered in metadata but errors loudly inside UE4SS's reflection
-- layer when read, spamming the log every 30s as we rescan). We
-- discover the working name on the first successful read, cache it,
-- and skip the others forever after. If no candidate works the cache
-- stays nil and we log a single warning instead of failing silently.
local ownerGuidField = nil           -- the field name that worked
local ownerGuidDiscoveryDone = false -- avoid re-warning on every refresh
local OWNER_GUID_CANDIDATES = {{
    -- Order matters: try the most likely current-build name first.
    -- "Net_OwnerCharacterGuid" is intentionally absent — it is
    -- metadata-mapped on MTCompany but UE4SS cannot retrieve it on
    -- this build, and even pcall can't suppress the engine-level
    -- error spam it produces.
    "OwnerCharacterGuid",
    "FounderCharacterGuid",
}}

local function GetCompanyOwnerGuid(co)
    if ownerGuidField then
        return SafeGet(co, ownerGuidField)
    end
    for _, name in ipairs(OWNER_GUID_CANDIDATES) do
        local v = SafeGet(co, name)
        if v ~= nil then
            ownerGuidField = name
            Log(string.format("MTCompany owner field discovered: %s", name))
            return v
        end
    end
    return nil
end

local function FindLocalPlayerCharacterGuid()
    -- Find the local PlayerController + the character GUID the player
    -- currently controls. In the company-system list, a company lists
    -- its owner by character GUID, so this is what we compare against.
    local pcs = FindAllOf("MotorTownPlayerController")
    if not pcs then return nil end
    local count = 0
    pcall(function() count = #pcs end)
    for i = 1, count do
        local pc = pcs[i]
        local isLocal = false
        pcall(function() isLocal = pc:IsLocalController() end)
        if isLocal then
            -- Try a few paths to get the character GUID.
            local ps = SafeGet(pc, "PlayerState")
            if ps then
                local cid = SafeGet(ps, "Net_OwnerCharacterId")
                if cid then
                    local g = SafeGet(cid, "CharacterGuid")
                    if g then return g end
                end
                local g2 = SafeGet(ps, "Net_OwnerCharacterGuid")
                if g2 then return g2 end
            end
        end
    end
    return nil
end

local function RefreshOwnedCompanies()
    ownedCompanies = {{}}
    local myCharacterGuid = FindLocalPlayerCharacterGuid()
    if not myCharacterGuid then
        return
    end

    local systems = FindAllOf("MTCompanySystem")
    if not systems then return end
    local sysCount = 0
    pcall(function() sysCount = #systems end)

    for s = 1, sysCount do
        local cs = systems[s]
        local companies = SafeGet(cs, "Server_Companies")
        if companies then
            local count = 0
            pcall(function() count = #companies end)
            for i = 1, count do
                local co = companies[i]
                -- Company's founding owner. Cached field name lookup —
                -- see GetCompanyOwnerGuid for the discovery logic.
                local ownerGuid = GetCompanyOwnerGuid(co)
                if GuidsEqual(ownerGuid, myCharacterGuid) then
                    local guid = SafeGet(co, "Guid")
                    if guid then
                        ownedCompanies[#ownedCompanies + 1] = {{
                            system = cs,
                            guid = guid,
                            name = SafeGet(co, "Net_CompanyName") or "<?>",
                        }}
                    end
                end
            end
        end
    end

    if #ownedCompanies > 0 then
        local names = {{}}
        for _, c in ipairs(ownedCompanies) do
            names[#names + 1] = tostring(c.name)
        end
        Log("Owned companies: " .. table.concat(names, ", "))
    elseif not ownerGuidDiscoveryDone then
        ownerGuidDiscoveryDone = true
        if ownerGuidField then
            -- Field name worked but no companies matched the local
            -- player's character GUID — they don't own any company yet.
            Log("No owned companies (matched field: " .. ownerGuidField
                .. ") — found a player but no company they own. "
                .. "Will fall back to local-controller match on hook fire.")
        else
            -- Every candidate field returned nil. This MT build doesn't
            -- expose the company-owner GUID under any name we know
            -- about. We fall back to a controller-match heuristic at
            -- hook time: if the RPC's `self` is the local player's
            -- controller, we boost. That's correct in single-player
            -- and accurate enough for self-hosting (the host's own
            -- deliveries get boosted, no one else's), and degrades
            -- to no-op on a dedicated server with no local player.
            Log("MTCompany owner-GUID field not exposed on this build. "
                .. "Falling back to local-controller match: deliveries "
                .. "made by the local player are boosted; deliveries "
                .. "from other players on the same server are not. "
                .. "Single-player + self-host work correctly. Dedicated "
                .. "servers with no local player will not boost anything.")
        end
    end
end

local function IsOwnedByMyCompany(vehicle)
    if not vehicle or #ownedCompanies == 0 then return false end
    local vg = SafeGet(vehicle, "Net_OwnerCompanyGuid")
    if not vg then return false end
    for _, c in ipairs(ownedCompanies) do
        if GuidsEqual(vg, c.guid) then
            return true, c
        end
    end
    return false, nil
end

-- Fallback ownership check used when MTCompany discovery fails.
-- Compares the hook's `self` PlayerController to the local player's
-- controller — if they match, the RPC was triggered by the local
-- player's own delivery, so the boost is correct to apply. This
-- bypasses MTCompany entirely.
local function IsHookCallerLocalPlayer(hookSelf)
    if not hookSelf then return false end
    local ok, isLocal = pcall(function() return hookSelf:IsLocalController() end)
    return ok and isLocal == true
end

-- ============================================================
-- Hook targets to try. Player path is confirmed; the AI-driver
-- paths are best-guess names — UE4SS RegisterHook fails silently
-- if a target doesn't exist, so the wrong ones drop out and the
-- right ones (if any) start firing. Add more here as we learn
-- the actual function names per MT build.
local HOOK_TARGETS = {{
    -- Player-controlled deliveries (confirmed working).
    "/Script/MotorTown.MotorTownPlayerController:ServerGiveOwnerProfitShare",
    -- AI driver candidates — these RPCs may not all exist on every
    -- build. The unsuccessful ones just don't register; pcall keeps
    -- the others alive.
    "/Script/MotorTown.MotorTownAIController:ServerGiveOwnerProfitShare",
    "/Script/MotorTown.MTAIController:ServerGiveOwnerProfitShare",
    "/Script/MotorTown.MTAICharacter:ServerGiveOwnerProfitShare",
    "/Script/MotorTown.MTCompanySystem:GiveOwnerProfitShare",
    "/Script/MotorTown.MTCompanySystem:Server_GiveOwnerProfitShare",
    "/Script/MotorTown.MotorTownGameMode:GiveOwnerProfitShare",
}}

-- Single shared boost handler used by every successfully-registered
-- hook. self may be a PlayerController, AIController, or CompanySystem
-- depending on which RPC fired — the logic doesn't care, it only
-- reads the vehicle parameter.
local function BoostHandler(hookName)
    return function(self, vehicleWrap, moneyWrap, txTypeWrap)
        local vehicle = Unwrap(vehicleWrap)
        if not vehicle then return end

        -- Primary path: company-membership check. Works when
        -- MTCompany's owner-GUID field is reflectable. AI driver
        -- vehicles are owned by a company, so this catches them too
        -- as long as discovery succeeded.
        local matched, company = IsOwnedByMyCompany(vehicle)
        local matchKind = "company"
        local matchLabel = nil

        -- Fallback path: when ownedCompanies is empty (MTCompany
        -- field not reflectable on this build), check whether the
        -- hook's `self` is the local player's controller. AI driver
        -- hooks won't pass this check — `self` would be the AI
        -- controller, not the local player — so AI deliveries only
        -- get boosted via the company-membership path. The fallback
        -- exclusively serves human-driver single-player + self-host.
        if not matched and #ownedCompanies == 0 and IsHookCallerLocalPlayer(self) then
            matched = true
            matchKind = "local-controller"
            company = nil
        end

        if not matched then return end
        matchLabel = company and tostring(company.name) or "(local player)"

        local money = Unwrap(moneyWrap) or 0
        if type(money) ~= "number" or money <= 0 then return end

        local boosted = math.floor(money * CONFIG.Multiplier)
        if boosted <= money then return end  -- no-op guard

        local setOk, setErr = pcall(function()
            moneyWrap:set(boosted)
        end)
        if setOk then
            Log(string.format(
                "boost applied: %d -> %d (x%.2f) via=%s match=%s/%s",
                money, boosted, CONFIG.Multiplier, hookName, matchKind, matchLabel))
        else
            Log("param :set() failed on " .. hookName .. ": " .. tostring(setErr)
                .. " (vanilla payout unchanged)")
        end
    end
end

-- Hook: try every candidate target. Each pcall'd registration that
-- succeeds adds another firing site for the boost. AI driver coverage
-- depends on whether MT exposes one of the candidate AI paths on the
-- user's build.
local function SetupHook()
    if math.abs(CONFIG.Multiplier - 1.0) < 1e-6 then
        Log("Multiplier = 1.0 -- no hooks installed (mod is a no-op).")
        return
    end
    local registered = 0
    local registeredNames = {{}}
    for _, target in ipairs(HOOK_TARGETS) do
        -- Short label for the log line: just the trailing
        -- ClassName:FunctionName, drop the /Script/Module. prefix.
        local label = target
        local lastSlash = string.find(string.reverse(target), "/", 1, true)
        if lastSlash then
            label = string.sub(target, #target - lastSlash + 2)
        end
        local ok, err = pcall(function()
            RegisterHook(target, BoostHandler(label))
        end)
        if ok then
            registered = registered + 1
            registeredNames[#registeredNames + 1] = label
        end
    end
    if registered == 0 then
        Log("WARNING: no hook targets registered. Boost will not fire.")
    else
        Log(string.format("Hooks registered (%d): %s",
            registered, table.concat(registeredNames, ", ")))
        Log("AI driver coverage: depends on which target fires for AI "
            .. "deliveries on this MT build. If your AI drivers' deliveries "
            .. "still aren't boosted, run UE4SS Live View, find the "
            .. "function that handles AI delivery rewards (look for "
            .. "'Profit', 'Reward', or 'Delivery' in MTAIController / "
            .. "MTCompanySystem / MotorTownGameMode), and report the "
            .. "exact name so we can add it to HOOK_TARGETS.")
    end
end

-- ============================================================
-- Init
-- ============================================================
Log("=== Cryovac Company Profit Boost Loading ===")
Log(string.format("Multiplier: x%.2f", CONFIG.Multiplier))

-- Seed the owned-companies list once things are loaded.
ExecuteWithDelay(15000, function()
    RefreshOwnedCompanies()
    SetupHook()
end)

-- Rescan periodically so a freshly-founded company starts benefiting
-- without forcing a relaunch.
LoopAsync(CONFIG.OwnerRescanEverySec * 1000, function()
    RefreshOwnedCompanies()
    return false  -- keep looping
end)

Log("=== Cryovac Company Profit Boost Loaded ===")
'''


def _validate(config: Dict[str, Any]) -> float:
    try:
        val = float(config.get('multiplier', DEFAULT_CONFIG['multiplier']))
    except (TypeError, ValueError):
        raise ValueError("multiplier must be numeric")
    if not 1.0 <= val <= 10.0:
        raise ValueError(f"multiplier {val} out of sanity range [1.0, 10.0]")
    return val


def generate_main_lua(config: Dict[str, Any]) -> str:
    val = _validate(config)
    return _MAIN_LUA_TEMPLATE.format(multiplier=f"{val:.3f}")


def generate_readme(config: Dict[str, Any]) -> str:
    val = _validate(config)
    if abs(val - 1.0) < 1e-6:
        effect = "no change (mod is a no-op, hook not installed)"
    else:
        pct = val * 100.0
        effect = f"deliveries in your own vehicles pay {pct:.0f}% of vanilla"
    body = (
        f"Multiplier: x{val:.2f}  ({effect})\n\n"
        "WHAT THIS MOD DOES\n"
        "------------------\n"
        "Pre-hooks MotorTownPlayerController:ServerGiveOwnerProfitShare.\n"
        "When it fires for a vehicle whose Net_OwnerCompanyGuid matches\n"
        "a company the local player owns, the mod rewrites the Money\n"
        "parameter to floor(Money * multiplier) BEFORE vanilla runs.\n"
        "Vanilla then pays out the boosted amount as a single\n"
        "transaction - the on-screen '+$X Owner's Profit Share' popup\n"
        "and the company transaction ledger both reflect the real total.\n\n"
        "Owner lookup walks MTCompanySystem.Server_Companies every 30s\n"
        "and picks out the ones whose OwnerCharacterGuid matches the\n"
        "local player's character GUID. Companies founded mid-session\n"
        "start getting the boost within the next 30s.\n\n"
        "Non-company vehicles (AI-company, rental, other players'\n"
        "companies) are ignored entirely - the hook short-circuits\n"
        "before touching the Money parameter.\n\n"
        "If the UE4SS param :set() call fails for any reason (rare), a\n"
        "warning is logged and the vanilla payment goes through\n"
        "unchanged - no stuck state, no double-credit.\n\n"
        "Runs server-side. On a dedicated server, install on the host.\n"
    )
    return render_install_readme(MOD_NAME, body)


def deploy(config: Dict[str, Any], output_dir: str = None) -> Dict[str, Any]:
    out_dir = output_dir or DEFAULT_OUTPUT_DIR
    val = _validate(config)
    return write_mod_folder(
        mod_name=MOD_NAME,
        output_dir=out_dir,
        main_lua=generate_main_lua(config),
        readme=generate_readme(config),
        mod_info={'multiplier': val, 'bonus_percent': (val - 1.0) * 100.0},
    )


